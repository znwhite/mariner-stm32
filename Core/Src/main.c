int __io_putchar(int ch)
{
  HAL_UART_Transmit(&huart2, (uint8_t *)&ch, 1, HAL_MAX_DELAY);
  return ch;
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  static uint32_t last_print_time = 0;
  static bool sentence_started = false;

  if (huart->Instance == USART3)
  {
    byte_count++;

    if (gps_rx_byte == '$')  // Start of a new sentence
    {
      gps_idx = 0;
      sentence_started = true;
      gps_buffer[gps_idx++] = gps_rx_byte;
    }
    else if ((gps_rx_byte == '\n' || gps_rx_byte == '\r') && sentence_started)
    {
      gps_buffer[gps_idx] = '\0';  // Null-terminate
      uint32_t now = HAL_GetTick();
      if (gps_idx > 6 && (now - last_print_time >= 10000))  // Sanity check
      {
        last_print_time = now;
        printf("GPS: %s\r\n", gps_buffer);
      }
      sentence_started = false;  // Reset for next sentence
    }
    else if (sentence_started && gps_idx < GPS_BUF_LEN - 1)
    {
      gps_buffer[gps_idx++] = gps_rx_byte;
    }

    // Re-arm the interrupt
    HAL_UART_Receive_IT(&huart3, &gps_rx_byte, 1);
  }
}


